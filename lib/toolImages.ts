export function getToolImage(toolName: string) {
  // Try to find exact match
  if (toolImages[toolName]) return toolImages[toolName];
  
  // Try case-insensitive and space-flexible match
  const normalizedKey = toolName.toLowerCase().replace(/\s/g, '');
  const match = Object.keys(toolImages).find(k => 
    k.toLowerCase().replace(/\s/g, '') === normalizedKey
  );
  
  if (match) return toolImages[match];

  return {
    name: toolName,
    image: `https://www.google.com/s2/favicons?domain=${toolName.toLowerCase().replace(/\s/g, '')}.com&sz=128`,
    description: 'Professional AI Tool'
  }
}

